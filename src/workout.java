public class workout {
    String name;
    int weight;
    String date;


    workout(){
        name = this.name;
        weight = this.weight;
        date = this.date;

    }

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public int getWeight(){
        return weight;
    }

    public void setWeight(int weight){
        this.weight = weight;
    }

    public String getDate(){
        return date;
    }

    public void setDate(String date){
        this.date = date;
    }
    
    
}
